template<typename T>
void maxs(T& x, T&& y) {
  x=std::max(x,y);
}
template<typename T>
void maxs(T& x, T& y) {
  x=std::max(x,y);
}

template<typename T>
void mins(T& x, T&& y) {
  x=std::min(x,y);
}
template<typename T>
void mins(T& x, T& y) {
  x=std::min(x,y);
}

int main() {
  ios::sync_with_stdio(false);
  std::cin.tie(nullptr);

  ll X;
  cin >> X;

  ll answer {};
  for (ll x = X;x != 0;) {
    ll max_c {1};
    ll tmp {1};
    while (tmp <= x) {
      tmp *= 6;
    }
    tmp /= 6;
    maxs(max_c, tmp);
    tmp = 1;
    while (tmp <= x) {
      tmp *= 9;
    }
    tmp /= 9;
    maxs(max_c, tmp);
    x -= max_c;
    ++answer;
  }
  cout << answer << '\n';
}
